function GenerateConstants(
  jsonpath,
  varname,
  component,
  stage,
  inheritance = '',
  deprecatedMsg = ''
) {
  const absolutePath = path.resolve(__dirname, '../../', jsonpath);
  const currentFile = JSON.parse(fs.readFileSync(absolutePath, 'utf-8'));
  const dataKeys = currentFile.flatMap((e) => {
    const primary = e[varname];
    const inherited = inheritance && e[inheritance] ? e[inheritance] : [];
    return [primary, ...[].concat(inherited)];
  });
  const uniqueKeys = [...new Set(dataKeys.filter(Boolean))];
  const SerializeKey = (str) => String(str).toUpperCase().replace(/-/g, '_');
  const target = './src/constants.js';

  let content = '';

  if (stage === 'entry') {
    content = '//AUTO GENERATED BY DYVIX UI ENGINE - DO NOT MANUALLY EDIT.\n\n';
  } else {
    content = fs.existsSync(target)
      ? fs.readFileSync(target, 'utf-8') + '\n'
      : '//AUTO GENERATED BY DYVIX UI ENGINE - DO NOT MANUALLY EDIT.\n\n';
  }

  content += deprecatedMsg !== '' ? `/**\n${deprecatedMsg}\n*/\n` : '';
  content += `export const DYVIX_${component.toUpperCase()}_${varname.toUpperCase()} = \n `;

  let constantObj = {};

  uniqueKeys.forEach((key) => {
    constantObj[SerializeKey(key)] = key;
  });

  content += JSON.stringify(constantObj, null, 2);

  fs.writeFileSync(target, content);
}
