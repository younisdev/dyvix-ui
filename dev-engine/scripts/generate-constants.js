import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

GenerateConstants('./src/components/modal/dependencies/themes.json', "theme", "modal", "entry");
GenerateConstants('./src/components/modal/dependencies/types.json', "type", "types", "post");
GenerateConstants('./src/components/animations.json', "animation", "global", "post");
GenerateConstants('./src/components/modal/dependencies/validator/validators.json', "preset", "modal_validation", "post");

function GenerateConstants(jsonpath, varname, component, stage)
{
  const absolutePath = path.resolve(__dirname, '../../', jsonpath);
  const currentFile = JSON.parse(fs.readFileSync(absolutePath, 'utf-8'));
  const dataKeys = currentFile.map(e => e[varname]);
  const SerializeKey = (str) => String(str).toUpperCase().replace(/-/g, '_');
  const target = "./src/constants.js";

  let content = ""
  
  if(stage === "entry")
  {
    content = '//AUTO GENERATED BY DYVIX UI ENGINE - DO NOT MANUALLY EDIT.\n\n'
  }
  else
  {
    content = fs.existsSync(target) ? 
    fs.readFileSync(target, 'utf-8') + '\n': 
    '//AUTO GENERATED BY DYVIX UI ENGINE - DO NOT MANUALLY EDIT.\n\n'
  }

  content += `export const DYVIX_${component.toUpperCase()}_${varname.toUpperCase()} = \n `

  let constantObj = {};

  dataKeys.forEach((key) => {
    constantObj[SerializeKey(key)] = key;
  });

  content += JSON.stringify(constantObj, null, 2);

  fs.writeFileSync(target, content);
}